                                </p>
                                <p>7.1. Točnost podatkov
                                </p>
                                <p>7.1.1. SPAR plus kartico je mogoče pridobiti le pod pogojem, da je prijavitelj družbi SPAR na pristopnici k tem Splošnim pogojem, s katero je s SPAR sklenil pogodbo o imetništvu SPAR plus kartice, posredoval naslednje točne osebne podatke, ki se nanašajo nanj: spol, ime, priimek, rojstni datum, podatke o prebivališču, elektronski naslov in številko mobilnega telefona. Pri posredovanju podatkov za pridobitev SPAR plus kartice ali pri kasnejših spremembah podatkov lahko vsaka fizična oseba odda le en aktualen elektronski naslov in le eno aktualno številko mobilnega telefona. Posamezen elektronski naslov oziroma posamezna številka mobilnega telefona se v bazi imetnikov SPAR plus kartice lahko vodi le pri enem imetniku SPAR plus kartice. SPAR te podatke potrebuje za komuniciranje z imetnikom in pripravo ter posredovanje ponudb in ugodnosti za imetnike, prilagojenih starosti, spolu in regiji prebivanja konkretnega imetnika.
                                </p><p>7.1.2. SPAR bo podatke o ugodnostih, popustih, dobroimetju in nakupih, opravljenih s predložitvijo SPAR plus kartice, pridobljene pred sprejemom teh splošnih pogojev, obdeloval v skladu s temi splošnimi pogoji le v primeru, da imetnik SPAR plus kartice, izdane pred sprejemom teh splošnih pogojev, k njim izrecno pristopi:
                                
                                </p><p> na prijavnih mestih s podpisom na tiskani pogodbi za prenovo imetništva SPAR plus kartice ali
                                preko Vozlišča SPAR plus.
                                
